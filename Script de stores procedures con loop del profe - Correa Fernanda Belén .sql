CREATE PROCEDURE ordenamiento_pf(campo_ordenar VARCHAR (40), IN orden BOOLEAN)
-- orden=1 -> asc
-- orden=0 -> desc
BEGIN
	IF campo_ordenar <> '' AND orden = 1 THEN
		SET @ordenar = concat('ORDER BY ', campo_ordenar);
	ELSEIF campo_ordenar <> '' AND orden = 0 THEN
		SET @ordenar = concat('ORDER BY ', campo_ordenar, ' DESC');
	ELSEIF campo_ordenar <> '' AND orden NOT IN (0,1) THEN
		SET @ordenar = 'No válido';
		SELECT 'Parámetro de ordenamiento ingresado no válido' AS Mensaje;
    ELSE
		SET @ordenar = '';
	END IF;
    IF @ordenar <> 'No válido' THEN
		SET @clausula_select = concat('SELECT * FROM clientes_tasa_37 ', @ordenar);
		PREPARE ejecucion FROM @clausula_select;
		EXECUTE ejecucion;
		DEALLOCATE PREPARE ejecucion;
	END IF;
END $$
delimiter ;

