/*Necesitamos saber el interés que devengará un plazo fijo, para eso creamos una funcion que permita calcular ingresando como parámetros
el capital, la tasa, y los días que se mantiene el plazo fijo, que a su vez se calculará como la diferencia entre la fecha de vencimiento
y la fecha de origen del plazo fijo */

#DROP FUNCTION IF EXISTS INTERES_DEVENGADO;
DELIMITER $$
CREATE FUNCTION INTERES_DEVENGADO(CAPITAL FLOAT, TASA FLOAT, fecha_vencimiento date, fecha_origen date)
RETURNS FLOAT
DETERMINISTIC
BEGIN
	DECLARE TASA_DECIMAL DECIMAL (3,3);
    DECLARE DIAS INT;
    DECLARE INTERESES FLOAT; 
    
    SET TASA_DECIMAL = TASA/100;
    SET DIAS = DATEDIFF(fecha_vencimiento, fecha_origen);
    SET INTERESES = CAPITAL * TASA_DECIMAL/365 * DIAS;
    
    RETURN INTERESES;
    
END$$
DELIMITER ;

